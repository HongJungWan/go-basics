package main

import "fmt"

func sum(numbers ...int) int {
	sum := 0

	for _, v := range numbers {
		sum += v
	}
	return sum
}

func main() {
	fmt.Println(sum(1, 2, 3))
	fmt.Println(sum(1, 2, 3, 4, 5, 6))
	fmt.Println(sum(1))
}

