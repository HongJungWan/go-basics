package main

import "fmt"

func main() {
	array := [5]int{1, 2, 3, 4, 5}
	slice := array[1:2]

	fmt.Println(array)
	fmt.Println(slice)

	array[1] = 100

	fmt.Println("\nAfter Change Element")
	fmt.Println(array)
	fmt.Println(slice)

	slice = append(slice, 500)

	fmt.Println("\nAfter Append 500 Element ")
	fmt.Println(array)
	fmt.Println(slice)
}

